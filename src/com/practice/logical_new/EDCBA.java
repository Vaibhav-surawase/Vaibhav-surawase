public class EDCBA {
//  E E E E E 
//  D D D D D
//  C C C C C
//  B B B B B
//  A A A A A   
	public static void main(String[] args)
	{
		for (char i='E'; i>='A'; i--)
		{
			for (char j='A'; j<=i;j++)
			{
					System.out.print(i);	
     		}
			System.out.println();
		}
		
	}
}
